/* estudo de variáveis do java */ 

public class VariablesStudie {
	public static void main(String[] args) {
		String nome = "André";
		int idade = 23;
		float nota1 = 7.5F;
		float nota2 = 5.0F;
		float media = (nota1 + nota2) / 2;
		boolean aprovado = true;


		System.out.println("Olá seu nome é: "+ nome);
		System.out.println("Sua idade é: " + idade);
		System.out.println("Sua Nota1 é: " + nota1);
		System.out.println("Sua Nota1 é: " + nota2);
		System.out.println("Sua Média é: " + media);
		System.out.println("O Status de Aprovação é: " + aprovado);
	}
}
